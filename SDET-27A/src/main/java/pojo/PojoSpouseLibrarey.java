package pojo;

public class PojoSpouseLibrarey {
	String name;
	int phno;
	String id;
	public PojoSpouseLibrarey(String name, int phno, String id) {
		super();
		this.name = name;
		this.phno = phno;
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPhno() {
		return phno;
	}
	public void setPhno(int phno) {
		this.phno = phno;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	

}
